package gunler.gun5_cuma21;

public class switchdersi {

    public static void main(String[] args) {
        int monat = 1;

        switch (monat) {
            case 1:
                System.out.println("januar");
                break;
            case 2:
                System.out.println("februa");
                break;
            case 3:
                System.out.println("marz");
                break;
            default:
                System.out.println("nicht im 1.quertal");
        }

    }
}
